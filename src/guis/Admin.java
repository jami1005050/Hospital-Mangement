    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        DoctorEntity doc = null;
        EmployeeEntity emp = null;
        textDes.setText(null);
        textDesg.setText(null);
        imagePane.setIcon(null);
        if(!chkDoc.isSelected() && !chkEmp.isSelected()) {
            msgAdmin.setText("Sorry!! Please choose one from the checkbox.");
        } else if(chkDoc.isSelected() && chkEmp.isSelected()) {
            msgAdmin.setText("Sorry!! Please choose one from the checkbox. Not both.");
        } else {
            int id=0;
            if(chkDoc.isSelected()) {
                try {
                    id = Integer.parseInt(inID.getText());
                } catch(Exception e) {
                    msgAdmin.setText("Enter number for ID");
                }
                try { 
                    doc = db.docDesg(id);
                } catch (SQLException ex) {
                    msgAdmin.setText("Invalid query. Please try again.");
                }
                if(doc == null) {
                    msgAdmin.setText("No profile matches your query. Try again.");
                } else {
                    textDes.setText("\tProfile Information\n\n");
                    textDes.append("Name: "+ doc.fname+" "+doc.lname+"\n\nEmail: "+doc.email+"\nAddress:\n"+ doc.address+"\nAcademic Description\n"+doc.academic+"\n");
                    if(doc.designation == null) {
                        textDesg.setText("<<No designation assigned.>>");
                    } else {
                        textDesg.setText(doc.designation);
                    }
                    ImageIcon image = new ImageIcon(doc.image);
                    imagePane.setIcon(image);
                }    
            } else if(chkEmp.isSelected()) {
                try {
                    id = Integer.parseInt(inID.getText());
                } catch(Exception e) {
                    msgAdmin.setText("Enter number for ID");
                }
                try { 
                    emp = db.empDesg(id);
                } catch (SQLException ex) {
                    msgAdmin.setText("Invalid query. Please try again.");
                }
                if(emp == null) {
                    msgAdmin.setText("No profile matches your query. Try again.");
                } else {
                    textDes.setText("\tProfile Information\n\n");
                    textDes.append("Name: "+ emp.fname+" "+emp.lname+"\n\nEmail: "+emp.email+"\nAddress:\n"+ emp.address+"\nAcademic Description\n"+emp.academic+"\n");
                    if(emp.designation == null) {
                        textDesg.setText("<<No designation assigned.>>");
                    } else {
                        textDesg.setText(emp.designation);
                    }
                    ImageIcon image = new ImageIcon(emp.image);
                    imagePane.setIcon(image);
                }
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

