    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        int type = typeCombo.getSelectedIndex();
        if(type == 0) {
            DoctorEntity doctor = new DoctorEntity(inFname.getText(),inLname.getText(),inFather.getText(),
                                inEmail.getText(),inAddress.getText(),inAcademic.getText(),Integer.parseInt(inPhone.getText()),
                                pupload.getSelectedFile());
            doctor.generateKeys();
            try {
                if(db.addDoctor(doctor)) {
                    msgAdmin.setText("Information is saved successfully.");
                    infoField.setText(doctor.generateSummary());
                    infoDialogue.setVisible(true);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                msgAdmin.setText("Database error occured during saving information!");;
            } catch (FileNotFoundException ex) {
                msgAdmin.setText("Error! Image file not found");
            }
        } else if(type == 1) {
            EmployeeEntity employee = new EmployeeEntity(inFname.getText(),inLname.getText(),inFather.getText(),
                                inEmail.getText(),inAddress.getText(),inAcademic.getText(),Integer.parseInt(inPhone.getText()),
                                pupload.getSelectedFile());
            employee.generatePassword();
            try {
                if(db.addEmployee(employee)) {
                    msgAdmin.setText("Information is saved successfully.");
                    infoField.setText(employee.generateSummary());
                    infoDialogue.setVisible(true);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                msgAdmin.setText("Database error occured during saving information!");;
            } catch (FileNotFoundException ex) {
                msgAdmin.setText("Error! Image file not found");
            }
        } else if(type == 2) {
            AdministratorEntity admin = new AdministratorEntity(inFname.getText(),inLname.getText(),inFather.getText(),
                                inEmail.getText(),inAddress.getText(),inAcademic.getText(),Integer.parseInt(inPhone.getText()),
                                pupload.getSelectedFile());
            admin.generateKeys();
            try {
                if(db.addAdmin(admin)) {
                    msgAdmin.setText("Information is saved successfully.");
                    infoField.setText(admin.generateSummary());
                    infoDialogue.setVisible(true);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                msgAdmin.setText("Database error occured during saving information!");;
            } catch (FileNotFoundException ex) {
                msgAdmin.setText("Error! Image file not found");
            }
        }
    }//GEN-LAST:event_btnCreateActionPerformed

